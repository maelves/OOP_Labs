public class Sofer {
    String nume;
    String prenume;
    int varsta;
    String nrPermis;

    public String getNume() {
        return nume;
    }

    public void setNume(String newNume) {
        this.nume = newNume;
    }

    public String getPrenume() {
        return prenume;
    }

    public void setPrenume(String newPrenume) {
        this.prenume = newPrenume;
    }

    public String getNrPermis() {
        return nrPermis;
    }

    public void setNrPermis(String newNr) {
        this.nrPermis = newNr;
    }

    public int getVarsta() {
        return varsta;
    }

    public void setVarsta(int newVarsta) {
        this.varsta = newVarsta;
    }
}
