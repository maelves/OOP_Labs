public class Talon {
    private String marcaModel;
    private String nrInmatriculare;
    private String anulInmastricularii;
    private String combustibil;
    private int putere;

    public void setMarcaModel(String marcaModel) {
        this.marcaModel = marcaModel;
    }

    public void setNrInmatriculare(String nrInmatriculare) {
        this.nrInmatriculare = nrInmatriculare;
    }

    public void setAnulInmastricularii(String anulInmastricularii) {
        this.anulInmastricularii = anulInmastricularii;
    }

    public void setCombustibil(String combustibil) {
        this.combustibil = combustibil;
    }

    public void setPutere(int putere) {
        this.putere = putere;
    }

    public String getMarcaModel() {
        return marcaModel;
    }

    public String getNrInmatriculare() {
        return nrInmatriculare;
    }

    public String getAnulInmastricularii() {
        return anulInmastricularii;
    }

    public String getCombustibil() {
        return combustibil;
    }

    public int getPutere() {
        return putere;
    }
}
