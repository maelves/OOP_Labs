public class Clasa {
    private String s;
    private float nr;

    public Clasa(String s, float nr) {
        this.s = s;
        this.nr = nr;
    }

    public String getS() {
        return s;
    }

    public float getNr() {
        return nr;
    }

    public void setS(String s) {
        this.s = s;
    }

    public void setNr(float nr) {
        this.nr = nr;
    }
}
